// @lc code=start
/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[]}
 */
var preorderTraversal = function(root) {
    var result = [];
    function get(node) {
        if(node){
            result.push(node.val)
            if(node.left)get(node.left);
            if(node.right)get(node.right);
        }
    }
    get(root);
    return result;
};
