// @lc code=start
/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
const levelOrder = root => {
    if (!root) return []
    let res = [], queue = [root]
    while (queue.length) {
        let arr = [], temp = []
        while (queue.length) {
            let curr = queue.shift()
            arr.push(curr.val)
            if (curr.left) temp.push(curr.left)
            if (curr.right) temp.push(curr.right)
        }
        queue = temp
        res.push(arr)
    }
    return res
}

